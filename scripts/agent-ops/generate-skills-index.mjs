import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';
import process from 'node:process';
import { agentOpsConfig } from './config.mjs';

// 递归扫描 SKILL.md。不存在的全局目录会被跳过。
function walkSkillFiles(root) {
  if (!existsSync(root)) {
    return [];
  }

  const results = [];
  const stack = [root];

  while (stack.length > 0) {
    const current = stack.pop();
    const entries = readdirSync(current, { withFileTypes: true });

    for (const entry of entries) {
      const fullPath = path.join(current, entry.name);

      if (entry.isDirectory()) {
        stack.push(fullPath);
        continue;
      }

      if (entry.isFile() && entry.name === 'SKILL.md') {
        results.push(fullPath);
      }
    }
  }

  return results.sort();
}

function parseFrontmatter(content) {
  if (!content.startsWith('---')) {
    return {};
  }

  const end = content.indexOf('\n---', 3);

  if (end === -1) {
    return {};
  }

  const lines = content.slice(3, end).split(/\r?\n/);
  const result = {};

  for (const line of lines) {
    const match = line.trim().match(/^([A-Za-z0-9_-]+):\s*(.*)$/);

    if (match) {
      result[match[1]] = match[2].trim().replace(/^["']|["']$/g, '');
    }
  }

  return result;
}

function wordsFrom(text) {
  const matches = text.toLowerCase().match(/[a-z0-9_/-]+|[\u4e00-\u9fa5]{2,}/g);
  return [...new Set(matches ?? [])].slice(0, 12);
}

function relativeSource(filePath) {
  const relative = path.relative(process.cwd(), filePath);

  if (!relative.startsWith('..') && !path.isAbsolute(relative)) {
    return relative.replaceAll(path.sep, '/');
  }

  const home = process.env.USERPROFILE || process.env.HOME || '';

  if (home) {
    const homeRelative = path.relative(home, filePath);

    if (!homeRelative.startsWith('..') && !path.isAbsolute(homeRelative)) {
      return `~/${homeRelative.replaceAll(path.sep, '/')}`;
    }
  }

  return path.basename(path.dirname(filePath));
}

function skillRows() {
  const files = agentOpsConfig.skillRoots.flatMap(walkSkillFiles);

  return files.map((filePath) => {
    const content = readFileSync(filePath, 'utf8');
    const frontmatter = parseFrontmatter(content);
    const name = frontmatter.name || path.basename(path.dirname(filePath));
    const description = frontmatter.description || content.split(/\r?\n/).slice(0, 8).join(' ');
    const keywords = wordsFrom(`${name} ${description}`).join(', ');

    return {
      keywords,
      capability: name,
      source: relativeSource(filePath),
      when: description || 'Use when this skill matches the task.',
    };
  });
}

function escapeCell(value) {
  return String(value).replaceAll('|', '\\|').replace(/\s+/g, ' ').trim();
}

export function buildSkillsIndexMarkdown() {
  const rows = skillRows().sort((a, b) =>
    `${a.capability}:${a.source}`.localeCompare(`${b.capability}:${b.source}`),
  );

  const markdown = [
    '# Skills Index',
    '',
    '> Generated by `python .\\verify.py agents --write`. Do not edit by hand.',
    '',
    '| Trigger Keywords | Capability | Source | When To Use |',
    '|---|---|---|---|',
    ...rows.map(
      (row) =>
        `| ${escapeCell(row.keywords)} | ${escapeCell(row.capability)} | ${escapeCell(
          row.source,
        )} | ${escapeCell(row.when)} |`,
    ),
    '',
  ].join('\n');

  return { markdown, count: rows.length };
}

export function writeSkillsIndex() {
  const { markdown, count } = buildSkillsIndexMarkdown();
  mkdirSync(path.dirname(agentOpsConfig.skillsIndexPath), { recursive: true });
  writeFileSync(agentOpsConfig.skillsIndexPath, markdown, 'utf8');
  console.log(`Wrote ${agentOpsConfig.skillsIndexPath} with ${count} entries.`);
}

if (fileURLToPath(import.meta.url) === path.resolve(process.argv[1])) {
  writeSkillsIndex();
}
